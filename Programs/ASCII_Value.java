package Programs;

public class ASCII_Value {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 'B';

        int Ascii1 = ch1;
        int Ascii2 = ch2;

        System.out.println("The ASCII value of " + ch1 + " is: " + Ascii1);
        System.out.println("The ASCII value of " + ch2 + " is: " + Ascii2);
    }
}
